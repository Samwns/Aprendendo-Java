// Passo 1: Hello World
// Um programa simples que imprime uma mensagem

public class Main {
  public static void main(String[] args) {
    System.out.println("Hello, World! ☕");
  }
}

/*
O que acontece aqui?
1. public class Main { } → Define a classe principal
2. public static void main(String[] args) { } → Método que executa
3. System.out.println("...") → Imprime uma mensagem
4. ; → Final da instrução

Execute este código! Você verá: Hello, World! ☕
*/