// Passo 1: Hello World - Seu primeiro programa Java!
// Este é o programa mais simples em Java

public class Main {
  public static void main(String[] args) {
    System.out.println("Hello, World! ☕");
  }
}

/*
EXPLICAÇÃO:

1. public class Main { }
   - Define a classe principal
   - Toda classe Java começa com a palavra 'class'
   - O nome da classe DEVE ser igual ao nome do arquivo

2. public static void main(String[] args) { }
   - Este é o método principal (entry point)
   - É onde o programa COMEÇA a executar
   - 'public' = pode ser acessado de qualquer lugar
   - 'static' = pertence à classe, não a objetos
   - 'void' = não retorna nada
   - 'String[] args' = argumentos de linha de comando

3. System.out.println("...");
   - Imprime uma mensagem na tela
   - 'System' = classe do sistema
   - 'out' = saída padrão (console)
   - 'println' = print + line (imprime e pula linha)
   - O ponto-e-vírgula (;) indica fim da instrução

RESULTADO:
Hello, World! ☕
*/

